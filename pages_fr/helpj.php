<p>
Цифрами обозначены: 
<ul>
<li><p>Пропорция  (в %)  данной реакции среди всех разных реакций данной географической зоны  
(кол-во этой реакции /кол-во разных реакций Х100), 
<li><p>
Пропорция  (в %) данной реакций среди всех реакций на данный стимул в этой зоне (кол-во 
этой реакции / кол-во ответов на данный стимул х100
</ul>
<td>
<p>Dans ce tableau les réactions sont présentées séparément, par ordre décroissant de fréquence. 
Les chiffres indiquent:
<ul>
<li><p>
Le taux (en %) de cette réaction parmi toutes les réactions de cette zone géographique (nombre 
d'occurences de cette réaction / nombre des réactions différentes  de cette zone x 100)
<li><p>
Le taux (en %) de cette réaction parmi les réactions à ce stimulus dans cette zone géographique 
(nombre d'occurences de cette réaction / nombre des réactions à ce stimulus x 100)
</ul>
</tr>
