<tr><td><b>ASSOC</b><td><b>ASSOC</b></tr>
<tr><td>
<p>
В поле поиска введите конкретный стимул+ enter
<p>
Если вводить только одну букву, Вы увидите все стимулы, начинающие с этой буквы (в 
алфавитном порядке)
<p>
В поле ответа фигурируют все реакции, данные на этот стимул, в обратном порядке
частотности (указана цифрой сразу после реакции). 
В первом столбце - все реакции, независимо от географического происхождения
В остальных столбцах, реакции распределены в зависимости от географического 
происхождения (Франция, Бельгия, Швейцария, Канада). 
Цветом обозначен факт совпадения данной реакции в двух или нескольких зон. Наводя 
курсор на реакцию, можно получить справку о том, где она еще встречается.
<p>
В скобках указаны: 
<ul>
<li><p>общее количество ответов, данных на данный  стимул, 
<li><p>количество  разных  реакций [в квардратных скобках дан процент разных ответов от их общего количества: тем меньше процент, тем больше единообразие в ответах респондентов],
<li><p>количество отказов,
<li><p>количество единичных  реакций.
</ul>
<td>
<p>Dans la fenêtre écrire un stimulus concret + enter
<p>
Si vous introduisez une seule lettre vous verrez tous les stimuli qui commencent par cette lettre 
(dans l'ordre alphabétique)
<p>
Dans la fenêtre de réponse apparaissent toutes les réactions données à ce stimulus, dans l'ordre 
décroissant de fréquence (indiquée par un chiffre après la réaction). 
Dans la première colonne sont données toutes les réactions indépendamment de la zone 
géographique.
Dans les autres colonnes les réactions sont réparties par zones de la Francophonie (France, 
Belgique, Suisse, Canada).  
Les couleurs indiquent si la réaction se retrouve également dans d'autres zones. En plaçant le 
curseur sur la réaction on peut également obtenir cette information.
<p>
Entre parenthèses sont indiqués: 
<ul>
<li><p>Le nombre total de réponses obtenues pour ce stimulus,
<li><p>Le nombre de réponses différentes [entre crochets on trouve le taux de réponses différentes pour le nombre total de réponses : plus il est faible, plus il y a d’unité dans les réponses des interviewés],
<li><p>Le nombre de refus de réponse,
<li><p>Le nombre de réponses uniques.
</ul>
</tr>
