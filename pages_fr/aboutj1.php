<p><b>DINAF</b> - Dictionnaire des normes associatives de la Francophonie
<p>
Dans cette étude nous nous proposons de comparer les réactions des différents locuteurs du 
français, dans quatre zones de la francophonie: Wallonie, Suisse romande, Canada francophone 
et France métropolitaine. Le but de cette comparaison est de mettre en évidence ce qui est 
commun aux locuteurs d'une même langue et ce qui ne l'est pas, d'apprécier à quel point une 
communauté linguistique partage une même "vision du monde" ou si cette dernière est une vue de 
l'esprit.
<p>
L’expérience qui a permis de réunir la matière pour le DENAF s’est déroulée sur internet du 15 septembre 2013 au 15 
septembre 2015. Pour cela il a été proposé par l’intermédiaire d’un questionnaire Google à des locuteurs du français 
sans distinction d'âge, lieu de résidence ou profession, de France, Belgique, Suisse et Canada de participer à une 
expérience de fixation de la première réaction à une liste de 100 mots, identiques pour tous. Ces mots font partie des 
plus courants de la langue française, selon les données du site http://eduscol.education.fr.
<p>Après traitement des données récoltées on a retenu 590 formulaires remplis pour la France, 473 pour la Belgique, 337 pour la Suisse et 456 pour le Canada. 
