<tr><td><p>
В обратном словаре представлены реакции, полученные в ходе эксперимента, и стимулы, 
вызвавшие их в порядке убывающей частотности. 
<p>
Выберите, в каком порядке Вы хотите располагать реакции: 
<ul>
<li><p><b>ВСЕ</b> - все полученные в ходе эксперимента реакции, алфавитном порядке.
<li><p><b>СТИМ</b> - по количеству разных стимулов, вызвавшие реакции (от 315  до 1) - 100 первых 
реакций, связанных с наибольшем количеством стимулов составляют т.н. это ядро языкового 
сознания.
<li><p><b>ЧАСТ</b>- по абсолютно частотности (от 2415 до 1).
<li><p><b>ИНД</b> - Вы также можете вводить одно слово или букву, чтобы посмотреть, на какие стимулы 
данное слово или слова, начинающие с этой буквы были даны в качестве реакции .
</ul>
<p>
Цифры в скобках указывают
<br>1- частотность реакции
<br>2- количество стимулов, на которые данное слово было дано в качестве реакции.
<td><p>
Le dictionnaire inverse présente les réactions réunies lors de l'expérience mise en regard des
stimuli qui l'ont évoquée (dans l'ordre de fréquence décroissante).
<p>
Choisissez ici dans quel ordre vous voulez présenter cette information:
<ul>
<li><p><b>TOTAL</b> - toutes les réactions obtenues dans l'expérience, par ordre alphabétique.
<li><p><b>STIM</b> - Par le nombre de stimuli différents ayant provoqué ces réactions (par ordre décroissant, de 
315 à 1). Les cent réactions liées au plus grand nombre de stimuli forment le "noyau linguistique 
de la langue" et jouent un rôle de "super-connecteurs" du réseau lexical.
<li><p><b>FREQ</b> - En fréquence absolue (par ordre décroissant, de 2415 occurences à 1).
<li><p><b>IND</b> - vous pouvez également proposer un mot en particulier ou une lettre de l'alphabet et voir à 
quel(s) stimulus (stimuli) ce mot ou les mots commençant par cette lettre sont apparus en réaction.
</ul>
<p>
Les chiffres entre parenthèses indiquent :
<br>Le nombre absolu d'occurences  de cette réaction,
<br>Le nombre de stimuli l'ayant provoquée.
